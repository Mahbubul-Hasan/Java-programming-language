public void game()
{
                                num1 = (int)(Math.random()*11);
				num2 = (int)(Math.random()*11);
				random = (int)(Math.random()*4);
				switch (random) 
				{
				case 0:
					questionField.setText(num1+ " + " +num2);
					realAns = num1 + num2;
					break;
				case 1:
					if(num1<num2)
					{
						int temp = num1;
						num1 = num2;
						num2 = temp;
					}
					questionField.setText(num1+ " - " +num2);
					realAns = num1 - num2;
					break;
				case 2:
					questionField.setText(num1+ " * " +num2);
					realAns = num1 * num2;
					break;
				case 3:
					if(num1<num2)
					{
						int temp = num1;
						num1 = num2;
						num2 = temp;
					}
                                       if (num1 ==0 || num2 ==0)
                                        {
                                            num1 += 2;
                                            num2 += 2;
                                        }
						questionField.setText(num1+ " / " +num2);
						realAns2 = (double) num1 / num2;
                                                realAns2 = Math.floor(realAns2 * 100)/100;
						break;
					

				  }
}
