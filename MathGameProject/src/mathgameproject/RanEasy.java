    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                                userAns2 = Double.parseDouble(ansField.getText());
                                userAns = Double.parseDouble(ansField.getText());
                                switch (random)
                                {
                                    case 0:
                                    summary = summary + "\n" +num1+ " + " +num2+ " = " +userAns+ " : "+(userAns == realAns);
                                    break;
                                    case 1:
                                    summary = summary + "\n" +num1+ " - " +num2+ " = " +userAns+ " : "+(userAns == realAns);
                                    break;
                                    case 2:
                                    summary = summary + "\n" +num1+ " * " +num2+ " = " +userAns+ " : "+(userAns == realAns);
                                    break;
                                    case 3:
                                    summary = summary + "\n" +num1+ " / " +num2+ " = " +userAns2+ " : "+(userAns2 == realAns2);
                                    break;
                                    
                                }
                                if (userAns2 == realAns2 || userAns == realAns)
				{
					right++;
                                        game();
					ansField.setText("");
                                }
                                else if (userAns != realAns)
				{
					
                                        wrong++;
                                        game();
					ansField.setText("");
					
				}
                                else if (userAns2 != realAns2)
				{
					
                                        wrong++;
                                        game();
					ansField.setText("");
					
				}
				if (right + wrong == numOfQues)
				{
					endTime = System.currentTimeMillis();
                                        totalTime = endTime - startTime;
                                        Result result = new Result();
                                        result.jTextArea.append(summary);
                                        result.jTextField1.setText(Integer.toString(right));
                                        result.jTextField1.setText(Integer.toString(wrong));
                                        result.jTextField2.setText(Long.toString(totalTime /1000));
                                        result.setVisible(true);
                                        setVisible(false);
				}
    }//GEN-LAST:event_jButton2ActionPerformed

